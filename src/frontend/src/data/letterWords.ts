/**
 * Get the word list for a specific letter
 * @param letter - A single uppercase letter A-Z
 * @returns Array of words starting with that letter
 */
export function getWordsForLetter(letter: string): string[] {
  const upperLetter = letter.toUpperCase();
  return letterWordsData[upperLetter] || [];
}

/**
 * Get all available letters that have word lists
 * @returns Array of uppercase letters A-Z
 */
export function getAvailableLetters(): string[] {
  return Object.keys(letterWordsData).sort();
}
